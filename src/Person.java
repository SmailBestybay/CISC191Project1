/**
 * 
 * @author Dre, Smail 
 * Represents a person. A person has a name. 
 */
public class Person
{
	//Fields
	private String name;
	
	//Constructors
	
	/**
	 * Default constructor for a Person
	 */
	public Person(String name)
	{
		this.name = name;
	}
	
	public String getName()
	{
		return name;
	}
}
